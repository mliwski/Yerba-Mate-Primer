package com.yerbamateprimer.model;

/**
 * This enumeration is useful to
 * indicate the sweetner of a 
 * yerba mate.
 * 
 * @author Matias Liwski
 * @version 1.0
 */
public enum Sweetness {
	BITTER,		// No sweetner is used.
	SUGAR,		// Sugar is used as sweetner.
	EDULCORANT;	// Edulcorant is used as sweetner.
}
